.DATA
	ARRAY: .WORD 1,6,17,20,25
	SEARCH: .WORD 5
	RESULT: .WORD 0

.TEXT
	LDR R0,=ARRAY
	LDR R1,=SEARCH
	LDR R7,=RESULT
	MOV R2,#1
	MOV R3,#4

LDR R4,[R1]
LOOP1:
	CMP R2,R3
	BEQ FINISH
	ADD R5,R2,R3
	MOV R5,R5,LSR #1
	MOV R5,R5,LSL #2
	LDR R6,[R0,R5]
	CMP R4,R6
	BEQ FOUND
	BLT LESSTHAN
	BGT GREATERTHAN
	B LOOP1

FOUND:
	MOV R8,#0
	STR R8,[R7]
	B END	

LESSTHAN:
	MOV R3,R5
	MOV R3,R3,LSR #2
	B LOOP1

GREATERTHAN:
	MOV R2,R5
	MOV R2,R2,LSR #2
	B LOOP1

FINISH:
	MOV R8,#1
	STR R8,[R7]
	B END

END:
	SWI 0X011